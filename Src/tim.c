// 定时器定时中断的回调函数
static  uint8_t  touchcount=0;
static  uint8_t  adccount=0;
static  uint8_t  s1_count=0;
static  uint8_t  ms100_count=0;
static  uint8_t  ms1_count=0;

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *tim_baseHandle)
{
    if(tim_baseHandle->Instance==TIM4)
    {
        ms100_count++;
		ms100_count > 9 ? ms100_count = 0 : ms100_count;
		
		//0.1s
		//tim_task.enable_ReadInf = ENABLE;
		tim_task.enable_SendHeart = ENABLE;
		
		
    }
    else if(tim_baseHandle->Instance==TIM5)
    {
		s1_count++;
		s1_count > 9 ? s1_count = 0 : s1_count;
		
//		tim_task.enable_ReadSensor = ENABLE;//1s
//		tim_task.enable_Led = ENABLE;
//		tim_task.enable_ReadRTC = ENABLE;
//		
//		if(s1_count == 0){tim_task.enable_SendReport = ENABLE;}//5s
		
		switch(s1_count)
		{
			case 0:
			case 5:
				tim_task.enable_SendReport = ENABLE;//5s
				tim_task.enable_PowerOn = ENABLE;
				tim_task.enable_CH4_onoff = ENABLE;
			case 1:
			case 2:
				
			default:
				//1s
				tim_task.enable_Led = ENABLE;
				tim_task.enable_ReadRTC = ENABLE;
				tim_task.enable_ReadInf = ENABLE;
				tim_task.enable_ReadWifiConf = ENABLE;
				tim_task.enable_SecOneTask = ENABLE;
				tim_task.win_count > 0?tim_task.win_count--:tim_task.win_count;//窗口计时器
				tim_task.net_conn_count++;
				tim_task.enable_ReadSensor = ENABLE;
		}
    }
    else if(tim_baseHandle->Instance==TIM7)
    {
		ms1_count++;
		ms1_count > 99 ? ms1_count = 0 : ms1_count;
		
		if(ms1_count % 10 == 0){tim_task.enable_ReadTouch = ENABLE;}//10ms touch
		if(ms1_count % 5 == 0){tim_task.enable_ReadWin = ENABLE;}//5ms win
		if(ms1_count % 100 == 0){tim_task.enable_SendHeart = ENABLE;
								}//100ms send heart bump mess
    }
}


